<template lang="pug">
svg(:class="svgClass" aria-hidden="true")
  use(:xlink:href="iconName")
</template>

<script>
export default {
  name: 'SvgIcon',
  props: {
    iconClass: {
      type: String,
      required: true
    },
    className: {
      type: String,
      default: ''
    }
  },
  computed: {
    iconName () {
      return `#icon-${this.iconClass}`
    },
    svgClass () {
      if (this.className) {
        return 'svg-icon ' + this.className
      } else {
        return 'svg-icon'
      }
    }
  }
}
</script>

<style scoped>
.svg-icon {
	width: 300px;
	height: 200px;
	fill: currentColor;
	overflow: hidden;
}
</style>
